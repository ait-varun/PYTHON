# Sequences
name = "Harry"

print(name[0]) # Prints the first character of the string i.e. H

print(name[1:4]) # Prints the characters from index 1 to index 4 i.e. arr

print(name[1:]) # Prints the characters from index 1 to the end of the string i.e. arry

print(name[:-1]) # Prints the characters from index 0 to index -1 i.e. Harr

print(name[-1]) # Prints the last character of the string i.e. y



names = ['Harry', 'Ron', 'Hermione', 'Ginny', 'Luna']

print(names) # Prints the entire list of names

print(names[0]) # Prints the first element of the list i.e. Harry